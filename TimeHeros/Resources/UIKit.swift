import UIKit

extension UIColor {
	
	@nonobjc class var white: UIColor {
		return UIColor(white: 1.0, alpha: 1.0)
	}
	
	@nonobjc class var grey: UIColor {
		return UIColor(white: 74.0 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var black: UIColor {
		return UIColor(white: 0.0, alpha: 1.0)
	}
	
	@nonobjc class var aquamarine: UIColor {
		return UIColor(red: 80.0 / 255.0, green: 227.0 / 255.0, blue: 194.0 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var purple: UIColor {
		return UIColor(red: 144.0 / 255.0, green: 19.0 / 255.0, blue: 254.0 / 255.0, alpha: 1.0)
	}
	
}

